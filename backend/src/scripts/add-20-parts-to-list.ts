async function add20PartsToList() {
  console.log('üå± Adding 20 parts to Parts List...\n');

  try {
    const createdParts = [];

    for (const partData of partsData) {
      const { stockQuantity, ...partFields } = partData;

      // Check if part already exists
      const existingPart = await prisma.part.findUnique({
        where: { partNo: partData.partNo }
      });

      if (existingPart) {
        console.log(`   ‚ö†Ô∏è  Part ${partData.partNo} already exists, skipping...`);
        continue;
      }

      // Create the part
      const part = await prisma.part.create({
        data: partFields
      });

      // Create stock entry
      await prisma.stock.create({
        data: {
          partId: part.id,
          quantity: stockQuantity,
          location: `Warehouse ${partData.rackNo.split('-')[0]}`
        }
      });

      createdParts.push(part);
      console.log(`   ‚úì Created: ${part.partNo} - ${part.description}`);
    }

    console.log(`\n‚úÖ Successfully added ${createdParts.length} parts to Parts List!`);
    console.log(`\nüìã Summary:`);
    console.log(`   - Parts Created: ${createdParts.length}`);
    console.log(`   - Total Parts in Database: ${await prisma.part.count()}`);

  } catch (error) {
    console.error('‚ùå Error adding parts:', error);
    throw error;
  } finally {
    await prisma.$disconnect();
  }
}

// Run the function
if (require.main === module) {
  add20PartsToList()
    .then(() => {
      console.log('\n‚úÖ Script completed successfully!');
      process.exit(0);
    })
    .catch((error) => {
      console.error('\n‚ùå Script failed:', error);
      process.exit(1);
    });
}

export { add20PartsToList };

